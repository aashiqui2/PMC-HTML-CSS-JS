//An anonymous function is a function that is defined without a name. In JavaScript, functions can be declared without a name and assigned to variables or used as arguments in other function calls.

// Anonymous function assigned to a variable
const add = function (a, b) {
    return a + b;
  };
  
  console.log(add(3, 5)); 

  //using arrow function 

  // Using an arrow function as an anonymous function
const multiply = (a, b) => a * b;

console.log(multiply(4, 7)); 

  