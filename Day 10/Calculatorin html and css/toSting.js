// JavaScript Array toString() Method
// The toString() method is used for converting and representing an array into string form. 
// It returns the string containing the specified array elements.


let array1 = ["Faizan","Haseeb","Suleiman"];
let array_2 = array1.toString();
console.log(array_2);
// OUTPUT: Faizan,Haseeb,Suleiman
console.log(typeof(array_2));
// OUTPUT: string
console.log(array1);
// OUTPUT: [ 'Faizan', 'Haseeb', 'Suleiman' ]  
// toString() doestn't make any changes in origina array